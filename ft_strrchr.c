#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	s += i;
	while (i >= 0)
	{
		if (*s == (unsigned char)c)
			return ((char*)s);
		s--;
		i--;
	}
	return (NULL);
}
